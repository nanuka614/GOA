# Dictionary ახსნა
# Dictionary არის მონაცემთა სტრუქტურა Python-ში, რომელიც შეინახავს წყვილებს "key: value".
# key-ები უნიკალური უნდა იყოს, ხოლო value შეიძლება ნებისმიერი ტიპის იყოს.
# მაგალითად: {"name": "Giga", "age": 25}


my_info = {
    "name": "გიგა",
    "surname": "კოჩალიძე",
    "age": 25
}
print("My info:", my_info)




keys = my_info.keys()
print("Keys:", keys)
values = my_info.values()
print("Values:", values)